    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "¿Confirma insertar la convocatoria " + perAca.getText() + " con id=" + convId.getText() + " y padre con id=" + aper.getText()) == 0) {
            String id = convId.getText();
            String periodo = "'"+perAca.getText()+"'";
            String fAper = "TO_DATE('"+aper.getText()+"','YYYY/MM/DD')";
            String fCierre = "'TO_DATE('"+cierre.getText()+"','YYYY/MM/DD')'";
            String state="'"+estado.getText()+"'";
            if (periodo.equals("")) {
                periodo = null;

            }
            if (fAper.equals("")) {
                fAper = null;
            }
            if(id.equals("")){
                id=null;
            }
            if (fCierre.equals("")) {
                fCierre = null;
            }
            if (state.equals("")) {
                state = null;
            }
            String comand = "insert into convocatoria values (" + id + ", " + periodo + "," + fAper + ","+fCierre+","+state+")";
            System.out.println(comand);
            query.ejecutar(comand);
            behaviors.setModel(query.query("select * from convocatoria", v));
            convId.setText("");
            perAca.setText("");
            aper.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

